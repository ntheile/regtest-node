#!/bin/sh
## Tail multiple logs.

set -E

###############################################################################
# Environment
###############################################################################

LOGS="
  /var/log/tor/notice.log
  /var/log/bitcoin/debug.log
  /var/log/lightning/lightningd.log
  /var/log/cl-rest.log
"

###############################################################################
# Methods
###############################################################################

check_logfile() {
  for log in $LOGS; do if [ -e "$log" ]; then echo $log; fi; done 
}

###############################################################################
# Script
###############################################################################

tail -f `check_logfile | tr '\n' ' '`